public class SayHi {
    // Given a String name , e.g "Bob " , return a greeting
    // of the "Hello Bob!" 
    // 
    // sayHi("Bob") -> "Hello Bob!" 
    // sayHi("Alice") -> "Hello Alice! " 
    // sayHi("X" -> "Hello X!" 
    
    public String sayHi(String name) {
        return "Hello " + name + "!" ; 
    }
}
